public class Demo_for {
    public static void main(String[] args) {
        int a = 1; // Starting point  
        
        while (a < 100){ // Juagement
            System.out.println(a);
            a+=2; // Iterations

        }
        System.out.println("The End for While !!!!!!!!!!!!!");

        for (int i = 1; i < 100; i++){
            System.out.println(i);
        }
        System.out.println("The End for for !!!!!!!!!!!!!!!!!!!!!!!!!!");
        
        
    }
}